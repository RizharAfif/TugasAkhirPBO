public void loadTable() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            String sql = "SELECT nilai_pkn.nama,total, nilai_ujian.totalNilai FROM nilai_pkn INNER JOIN nilai_ujian ON nilai_ujian.nama = nilai_pkn.nama;";
            Statement n = con.createStatement();
            ResultSet rs = n.executeQuery(sql);
            while (rs.next()) {
                Object[] o = new Object[3];
                o[0] = rs.getString("nilai_pkn.nama");
                o[1] = rs.getString("total");
                o[2] = rs.getString("totalNilai");
                model.addRow(o);
            }
        } catch (Exception e) {
        }
    }
