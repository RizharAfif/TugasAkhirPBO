    private void htgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_htgActionPerformed
        try {
            String nama = name.getText();
            int pkn = Integer.parseInt(perusahaan.getText());
            int nilai_ujian = Integer.parseInt(ujian.getText());
            double total = pkn * 0.6;
            double totalUjian = nilai_ujian * 0.4;
            double hasil = (int) total + totalUjian;         

            String sql = "insert into hasil_akhir (nama,totalPkn, totalUjian, hasilAkhir)"
                    + "VALUES ('" + nama + "','" + pkn + "', '" + nilai_ujian + "', '" + hasil + "')";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.execute();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
            ps.close();
            reset.doClick();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data gagal" + e);
        }
        loadTable();
    }//GEN-LAST:event_htgActionPerformed

